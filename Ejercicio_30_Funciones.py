# ¿Qué es una función?
#Una función es un bloque de código reutilizable que realiza una tarea específica. Se define con def y puede recibir parámetros y devolver valores.

def saludar():
    print("¡Hola! Bienvenido a Python.")

saludar()  # Llamamos a la función




# Explicación:

# def saludar(): define la función.
# print("¡Hola! Bienvenido a Python.") imprime un mensaje.
# saludar() ejecuta la función