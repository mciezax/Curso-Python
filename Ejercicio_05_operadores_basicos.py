#Exponenciación

print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)


#Multiplicación

print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)

#División
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)

#División entera
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)

#Residuo (módulo)
print(14 % 4)
print(12 % 4.5)

#Suma
print(-4 + 4)
print(-4. + 8)

print((-2 / 4), (2 / 4), (2 // 4), (-2 // 4))







